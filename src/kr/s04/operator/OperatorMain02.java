package kr.s04.operator;

public class OperatorMain02 {
	public static void main(String[] args) {
		System.out.println("===산술연산자===");
		System.out.println(10 + 10);
		System.out.println(20 - 7);
		System.out.println(10 * 3);
		System.out.println(10 / 3); //나눗셈(몫 구하기) / int/int=int
		System.out.println(5 % 3); //나머지 구하기
		System.out.println("-------------------");
		
		System.out.println(10.0 / 3); //3.3333 으로 출력됨 / double/int = double
		
	}
}
