package kr.s01.basic;
public class HelloWorld2 { // Class 명 (반드시 대문자로 시작해야함)
	public static void main(String[] args) { // main 함수 시작점, 프로그램 진입점이라 필수적으로 기입해야함
		System.out.println("오늘은 화요일");
		System.out.println("내일은 수요일");
	}
}

// 기호 의미 설명
// {} = 영역을 나타냄. (class/ main 영역의 시작과 끝을 나타내는 것처럼 영역을 나타내는 기호임)
// () = 함수 값
// ; = 한 라인의 끝을 나타내는 기호. 라인 끝에 필수적으로 붙여야함